str = "programming"
print(str)


# getting characters from index 1 to 4: 'poin'  
print("str[1:5] =", str[1:5])  
  
# getting characters from beginning to index 3: 'Tpoi'  
print("str[:4] =", str[:4])  
  
# getting characters from index 4 to end: 'nt Tech'  
print("str[4:] =", str[4:])  
  
# reversing a string: 'hceT tniopT'  
print("str[::-1] =", str[::-1])  